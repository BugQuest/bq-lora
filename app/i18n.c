const char *tr(str_id_t id)
{
    if ((unsigned)id >= STR_COUNT) return "?";
    const char *lang = settings_language();
    const char *s = (lang && lang[0] == 'e') ? EN[id] : FR[id];
    if (!s) s = FR[id];
    return s ? s : "?";
}
